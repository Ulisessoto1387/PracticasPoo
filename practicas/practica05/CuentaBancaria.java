public class CuentaBancaria {
    //Atributos
    private float saldo; 
    private String clave; 

    //Set y get de los atributos 
    public void setSaldo(float saldo){
        this.saldo = saldo; 
    }
    public float getSaldo(){
        return saldo;
    }

    public void setClave(String clave){
        this.clave = clave; 
    }
    public String getClave(){
        return clave; 
    }


}